int		ft_sqrt(int nb)
{
	long long int	i;

	if (nb == 1)
		return (1);
	if (nb > 1000000000)
		i = nb / 10000;
	else
		i = nb / 2;
	while (i > 0)
	{
		if (i * i == nb)
			return (i);
		i--;
	}
	return (0);
}
